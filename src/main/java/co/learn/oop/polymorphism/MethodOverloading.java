package co.learn.oop.polymorphism;
/*Compile-Time Polymorphism  ===   Static Polymorphism
 When a method is created with different numbers of parameters is called Method Overloading.
  "A class may define multiple methods with the same name and the return type but with different numbers of parameters/arguments
  or with different same numbers but different data types."

  for eg:
  public float area(float length, float width); // for rectangle
  public float area(float radius); //for circle

 */

public class MethodOverloading {

    public float area(float len, float width){

return (2.2f);
    }
    public int area(float len, float width, int random){
return (2);
    }


}
